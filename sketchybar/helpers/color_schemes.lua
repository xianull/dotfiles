-- Get scheme names list, grouped by light/dark
local function get_scheme_names()
  local dark = {}
  local light = {}
  for key, scheme in pairs(schemes) do
    if scheme.light then
      table.insert(light, key)
    else
      table.insert(dark, key)
    end
  end
  table.sort(dark)
  table.sort(light)
  return dark, light
end

-- Get current scheme from file
local function get_current_scheme()
  local file = io.open(os.getenv("HOME") .. "/.config/sketchybar/.theme", "r")
  if file then
    local theme = file:read("*l")
    file:close()
    if theme and schemes[theme] then
      return theme
    end
  end
  return "tokyo_night"
end

-- Save current scheme to file
local function save_current_scheme(name)
  local file = io.open(os.getenv("HOME") .. "/.config/sketchybar/.theme", "w")
  if file then
    file:write(name)
    file:close()
  end
end

-- Get colors for current scheme
local function get_colors()
  local current = get_current_scheme()
  local scheme = schemes[current]

  local with_alpha = function(color, alpha)
    if alpha > 1.0 or alpha < 0.0 then return color end
    return (color & 0x00ffffff) | (math.floor(alpha * 255.0) << 24)
  end

  return {
    black = scheme.black,
    white = scheme.white,
    red = scheme.red,
    green = scheme.green,
    blue = scheme.blue,
    yellow = scheme.yellow,
    orange = scheme.orange,
    magenta = scheme.magenta,
    cyan = scheme.cyan,
    grey = scheme.grey,
    transparent = scheme.transparent,
    bar = scheme.bar,
    popup = scheme.popup,
    bg1 = scheme.bg1,
    bg2 = scheme.bg2,
    bg3 = scheme.bg3,
    with_alpha = with_alpha,
  }
end

return {
  schemes = schemes,
  get_scheme_names = get_scheme_names,
  get_current_scheme = get_current_scheme,
  save_current_scheme = save_current_scheme,
  get_colors = get_colors,
}
